package io.ht.build.plugin.tasks

import org.gradle.api.DefaultTask
import org.gradle.api.GradleException
import org.gradle.api.file.DirectoryProperty
import org.gradle.api.provider.Property
import org.gradle.api.tasks.Input
import org.gradle.api.tasks.Internal
import org.gradle.api.tasks.Optional
import org.gradle.api.tasks.TaskAction
import org.gradle.work.DisableCachingByDefault
import java.io.File
import java.io.IOException
import java.util.concurrent.TimeUnit

/**
 * Creates the local kind cluster that backs the SUT (System Under Test).
 *
 * Behaviour:
 *  - Verifies the `kind` binary is on PATH.
 *  - Skips creation if a cluster with the configured name already exists (idempotent).
 *  - Otherwise runs `kind create cluster --name <name>` with optional `--image` and `--config`.
 *  - If no [configFile] is provided and [workers] > 0, auto-generates a kind config with
 *    one control-plane node plus N worker nodes (written to [generatedConfigDir]). This is
 *    needed on recent kind versions where the control-plane carries a `NoSchedule` taint.
 */
@DisableCachingByDefault(because = "Cluster creation has external side effects; idempotency is handled in the task itself.")
abstract class StartSutTask : DefaultTask() {
    @get:Input
    @get:Optional
    abstract val clusterName: Property<String>

    @get:Input
    @get:Optional
    abstract val nodeImage: Property<String>

    @get:Input
    @get:Optional
    abstract val configFile: Property<String>

    @get:Input
    @get:Optional
    abstract val workers: Property<Int>

    @get:Internal
    abstract val generatedConfigDir: DirectoryProperty

    @get:Input
    abstract val kindBinary: Property<String>

    @TaskAction
    fun startSut() {
        val kind = kindBinary.get()
        val name =
            clusterName.orNull?.takeIf { it.isNotBlank() }
                ?: throw GradleException(
                    "[startSut] sut.cluster.namespace is not configured. " +
                        "Add `sut { cluster { namespace = \"<name>\" } }` to your build script.",
                )

        ensureKindAvailable(kind)

        if (clusterExists(kind, name)) {
            logger.lifecycle("[startSut] kind cluster '{}' already exists — nothing to do.", name)
            return
        }

        val cmd = mutableListOf(kind, "create", "cluster", "--name", name)
        nodeImage.orNull?.takeIf { it.isNotBlank() }?.let { image ->
            cmd += listOf("--image", image)
        }

        val explicitConfig = configFile.orNull?.takeIf { it.isNotBlank() }
        val effectiveConfig: File? =
            when {
                explicitConfig != null -> {
                    val f = File(explicitConfig)
                    if (!f.isFile) {
                        throw GradleException("[startSut] kind config file not found: ${f.absolutePath}")
                    }
                    f
                }
                workers.getOrElse(0) > 0 -> generateKindConfig(workers.get())
                else -> null
            }
        effectiveConfig?.let { cmd += listOf("--config", it.absolutePath) }

        logger.lifecycle("[startSut] creating kind cluster '{}': {}", name, cmd.joinToString(" "))
        val result = exec(cmd, inheritIo = true)
        if (result.exitCode != 0) {
            throw GradleException("[startSut] kind create cluster failed with exit code ${result.exitCode}")
        }
        logger.lifecycle("[startSut] kind cluster '{}' is ready.", name)
    }

    /** Writes a minimal kind cluster config with 1 control-plane + N worker nodes. */
    private fun generateKindConfig(workerCount: Int): File {
        val dir = generatedConfigDir.get().asFile
        dir.mkdirs()
        val target = File(dir, "kind-cluster.yaml")
        val workerLines = (1..workerCount).joinToString("\n") { "  - role: worker" }
        target.writeText(
            """
            # Auto-generated by io.ht.build.plugin (sut.cluster.workers=$workerCount).
            # Do not edit by hand — set sut.cluster.configFile to use a custom config instead.
            kind: Cluster
            apiVersion: kind.x-k8s.io/v1alpha4
            nodes:
              - role: control-plane
            $workerLines
            """.trimIndent() + "\n",
            Charsets.UTF_8,
        )
        logger.lifecycle("[startSut] generated kind config with {} worker(s) at {}", workerCount, target.absolutePath)
        return target
    }

    private fun ensureKindAvailable(kind: String) {
        val result = exec(listOf(kind, "version"))
        if (result.exitCode != 0) {
            throw GradleException(
                "[startSut] '$kind' is not available on PATH or failed to run. " +
                    "Install kind: https://kind.sigs.k8s.io/",
            )
        }
    }

    private fun clusterExists(
        kind: String,
        name: String,
    ): Boolean {
        val result = exec(listOf(kind, "get", "clusters"))
        if (result.exitCode != 0) {
            throw GradleException("[startSut] failed to list kind clusters: ${result.stderr}")
        }
        return result.stdout.lineSequence().any { it.trim() == name }
    }

    private data class ExecResult(
        val exitCode: Int,
        val stdout: String,
        val stderr: String,
    )

    private fun exec(
        cmd: List<String>,
        inheritIo: Boolean = false,
    ): ExecResult {
        val pb = ProcessBuilder(cmd).redirectErrorStream(false)
        if (inheritIo) pb.inheritIO()
        return try {
            val process = pb.start()
            val stdout = if (inheritIo) "" else process.inputStream.readBytes().toString(Charsets.UTF_8)
            val stderr = if (inheritIo) "" else process.errorStream.readBytes().toString(Charsets.UTF_8)
            if (!process.waitFor(10, TimeUnit.MINUTES)) {
                process.destroyForcibly()
                throw GradleException("[startSut] command timed out: ${cmd.joinToString(" ")}")
            }
            ExecResult(process.exitValue(), stdout, stderr)
        } catch (e: IOException) {
            ExecResult(127, "", e.message ?: "")
        }
    }

    companion object {
        const val NAME = "startSut"

        /** Default number of worker nodes when no [configFile] is provided. */
        const val DEFAULT_WORKERS: Int = 1
    }
}
